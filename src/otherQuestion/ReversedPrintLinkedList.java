    static void reverse(ListNode head) {
        if (null == head || null == head.next) {
            return;
        }

        ListNode prev = null;
        ListNode pcur = head.next;
        ListNode next;

        while (pcur != null) {
            if (pcur.next == null) {
                pcur.next = prev;
                break;
            }
            next = pcur.next;
            pcur.next = prev;
            prev = pcur;
            pcur = next;
        }

        head.next = pcur;
        ListNode tmp = head.next;
        while (tmp != null) {
            System.out.println(tmp.val + "->");
            tmp = tmp.next;
        }
    }

